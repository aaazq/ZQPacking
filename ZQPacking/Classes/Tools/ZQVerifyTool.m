+ (BOOL)validatePassword:(NSString *)password
{
    //6-20位数字,字母,字符
    //    if(![self validateByRegex:@"^[a-zA-Z0-9]{6,15}$" withObject:password]){
    //        return NO;
    //    }
    if (password.length <6 || password.length >16){
        return NO;
    }
    //字母和数字
    NSString *kAlpha = @"ABCDEFGHIJKLMNOPQRSTUVWXYZabcdefghijklmnopqrstuvwxyz";
    NSString *kNum = @"0123456789";
    NSCharacterSet *cs1 = [[NSCharacterSet characterSetWithCharactersInString:kAlpha] invertedSet];
    NSCharacterSet *cs2 = [[NSCharacterSet characterSetWithCharactersInString:kNum] invertedSet];
    if([password rangeOfCharacterFromSet:cs1].location == NSNotFound || [password rangeOfCharacterFromSet:cs2].location == NSNotFound){
        return NO;
    }else{
        return YES;
    }
}

